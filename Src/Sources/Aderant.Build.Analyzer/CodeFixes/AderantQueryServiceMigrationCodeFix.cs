        private const string Title = "Make new style query service call";

        public sealed override ImmutableArray<string> FixableDiagnosticIds => ImmutableArray.Create(AderantQueryServiceMigrationRule.DiagnosticId);

        public sealed override FixAllProvider GetFixAllProvider() {
            return WellKnownFixAllProviders.BatchFixer;
        }


        public sealed override async Task RegisterCodeFixesAsync(CodeFixContext context) {
            var root = await context.Document.GetSyntaxRootAsync(context.CancellationToken).ConfigureAwait(false);

            var diagnostic = context.Diagnostics.First();
            var diagnosticSpan = diagnostic.Location.SourceSpan;

            // Find the type declaration identified by the diagnostic.
            //var queryExpression = root.FindToken(diagnosticSpan.Start).Parent.AncestorsAndSelf().OfType<MemberAccessExpressionSyntax>().FirstOrDefault();
            var queryExpression = root.FindToken(diagnosticSpan.Start).Parent as IdentifierNameSyntax;
            if (queryExpression == null) {
                return;
            }

            // Register a code action that will invoke the fix. 
            context.RegisterCodeFix(
                CodeAction.Create(
                    title: Title,
                    createChangedDocument: c => ChangeQuerySyntax(context.Document, queryExpression, c),
                    equivalenceKey: Title),
                diagnostic);
        }

        private async Task<Document> ChangeQuerySyntax(Document document, IdentifierNameSyntax queryExpressionNode, CancellationToken cancellationToken) {
            if (queryExpressionNode == null) {
                return document;
            }

            var viewModelSetName = queryExpressionNode.Identifier.Text;
            var root = await document.GetSyntaxRootAsync(cancellationToken);
            // nice parsing article here http://blog.comealive.io/Syntax-Factory-Vs-Parse-Text/
            var pluralIndex = Array.LastIndexOf(plurals, viewModelSetName); // last index in case singular and plural are the same
            string viewModelName;
            if (pluralIndex > 1) {
                viewModelName = plurals[pluralIndex - 1];
            } else {
                // not in the table make a primitive guess and they must fix by hand
                if (viewModelSetName.EndsWith("ies", StringComparison.Ordinal)) {
                    viewModelName = viewModelSetName.Replace("ies", "y");
                } else {
                    viewModelName = viewModelSetName.Remove(viewModelSetName.Length - 1);
                }
            }

            if (viewModelName == "Client" || viewModelName == "Matter") { // because we like namespaces and models to have the same name
                viewModelName = "ViewModels." + viewModelName;
            }

            var parentNode = queryExpressionNode.Parent;
            var leadingTrivia = parentNode.GetLeadingTrivia();
            var trailingTrivia = parentNode.GetTrailingTrivia();
            var replacementstring = $"Query<{viewModelName}>()";
            string expression = string.Empty;
            foreach (var node in parentNode.ChildNodesAndTokens()) {
                var nodeLeadingTrivia = node.GetLeadingTrivia().ToFullString();
                var nodeTrailingTrivia = node.GetTrailingTrivia().ToFullString();
                expression += nodeLeadingTrivia;
                if (node.Equals(queryExpressionNode)) {
                    expression += replacementstring;
                } else {
                    expression += node.ToString();
                }
                expression += nodeTrailingTrivia;
            }

            var replacementExpression = SyntaxFactory.ParseExpression(expression).WithLeadingTrivia(leadingTrivia).WithTrailingTrivia(trailingTrivia);
            var newRoot = root.ReplaceNode(parentNode, replacementExpression);
            var newDocument = document.WithSyntaxRoot(newRoot);
            return newDocument;
            //return document;

        }

